// 실데이터 앵커 인덱스
const IDX = { y1: 2, m3: 11, m1: 13, now: 14 }; // 2025-03, 2025-12, 2026-02, 2026-03

/** 4개 실데이터 앵커 사이를 선형 보간하여 15개월 히스토리 생성 */
function buildHistory(
  p_y1: number,   // 1년 전 (2025-03)
  p_m3: number,   // 3개월 전 (2025-12)
  p_m1: number,   // 1개월 전 (2026-02)
  p_now: number,  // 현재 (2026-03)
  unit: string
): { date: string; price: number; isReal?: boolean }[] {
  // 앵커: [idx, price]
  const anchors: [number, number][] = [
    [0,              lerp(0, IDX.y1, p_y1 * 0.92, p_y1, 0)],  // 2025-01 추정 (1년전보다 약간 낮음)
    [IDX.y1,  p_y1],
    [IDX.m3,  p_m3],
    [IDX.m1,  p_m1],
    [IDX.now, p_now],
  ];

  return DATES.map((date, i) => {
    // 두 앵커 사이 선형 보간
    let price = p_now;
    for (let a = 0; a < anchors.length - 1; a++) {
      const [i0, v0] = anchors[a];
      const [i1, v1] = anchors[a + 1];
      if (i >= i0 && i <= i1) {
        const t = (i - i0) / (i1 - i0);
        price = v0 + (v1 - v0) * t;
        break;
      }
    }
    const isAnchor = anchors.some(([ai]) => ai === i);
    return { date, price: parseFloat(price.toFixed(4)), ...(isAnchor ? { isReal: true } : {}) };
  });
}

function lerp(i0: number, i1: number, v0: number, v1: number, i: number): number {
  if (i1 === i0) return v0;
  return v0 + (v1 - v0) * ((i - i0) / (i1 - i0));
}

/** 국가 객체 생성 헬퍼
 * 가격: GlobalPetrolPrices.com 실데이터 (USD/L, USD/kWh)
 * 히스토리 앵커: [1년전, 3개월전, 1개월전, 현재]
 */
function makeCountry(
  id: string, nameKo: string, region: string, lat: number, lng: number,
  // 현재 실데이터 (2026-03-16)
  gasNow: number, dieselNow: number, elecNow: number, lpgNow: number,
  // 휘발유 히스토리 앵커 [y1, m3, m1] (없으면 현재가 기반 추정)
  gasY1: number, gasM3: number, gasM1: number,
  // LNG (국제현물, 국가별 아님 → 글로벌 같은 값)
  ngNow: number
): Country {
  const gasHistory    = buildHistory(gasY1, gasM3, gasM1, gasNow, 'USD/L');
  // 경유는 휘발유와 유사 패턴 (비율 유지)
  const dieselRatio   = dieselNow > 0 ? dieselNow / gasNow : 1;
  const dieselHistory = gasHistory.map(h => ({ date: h.date, price: parseFloat((h.price * dieselRatio).toFixed(4)) }));
  // 전력: 글로벌 데이터는 장기 평균이므로 전쟁 기간만 소폭 반영
  const elecHistory   = buildHistory(elecNow * 0.88, elecNow * 0.93, elecNow * 0.97, elecNow, 'USD/kWh');
  // LPG
  const lpgHistory    = lpgNow > 0
    ? buildHistory(lpgNow * 0.90, lpgNow * 0.94, lpgNow * 0.97, lpgNow, 'USD/L')
    : DATES.map(d => ({ date: d, price: 0 }));
  // LNG: 글로벌 시장가 기준, 국가 맞춤 없음 → 공통 앵커
  const ngHistory     = buildHistory(ngNow * 0.72, ngNow * 0.82, ngNow * 0.91, ngNow, 'USD/MMBtu');

  // baseline = 1년 전 값 (전쟁 전 기준)
  return {
    id, nameKo, region, lat, lng,
    prices: {
      gasoline: {
        baseline: gasY1,
        current: gasNow,
        unit: 'USD/L',
        history: gasHistory,
      },
      diesel: {
        baseline: parseFloat((gasY1 * dieselRatio).toFixed(4)),
        current: dieselNow,
        unit: 'USD/L',
        history: dieselHistory,
      },
      lng: {
        baseline: parseFloat((ngNow * 0.72).toFixed(3)),
        current: ngNow,
        unit: 'USD/MMBtu',
        history: ngHistory,
      },
      elec_residential: {
        baseline: parseFloat((elecNow * 0.88).toFixed(4)),
        current: elecNow,
        unit: 'USD/kWh',
        history: elecHistory,
      },
      elec_industrial: {
        baseline: parseFloat((elecNow * 0.88 * 0.82).toFixed(4)),
        current: parseFloat((elecNow * 0.82).toFixed(4)),
        unit: 'USD/kWh',
        history: elecHistory.map(h => ({ date: h.date, price: parseFloat((h.price * 0.82).toFixed(4)) })),
      },
      lpg: {
        baseline: parseFloat((lpgNow * 0.90).toFixed(4)),
        current: lpgNow,
        unit: 'USD/L',
        history: lpgHistory,
      },
    },
  };
}

