// ── 히스토리 생성 (2025-01 ~ 2026-03, 15개월) ────────────────────────────────
// realCurrent: GlobalPetrolPrices.com 2026-03-16 실제값
// warMult: 전쟁(2026-03~) 가격 급등 배수
// 기준값(baseline) = 2026-02 이전 평균 추정 (전쟁 전)
function genHistory(
  baseline: number,
  warMult: number,
  noise: number = 0.025
): { date: string; price: number }[] {
  const dates = [
    '2025-01','2025-02','2025-03','2025-04','2025-05','2025-06',
    '2025-07','2025-08','2025-09','2025-10','2025-11','2025-12',
    '2026-01','2026-02','2026-03',
  ];
  return dates.map((date, i) => {
    const warEffect = i >= 14 ? warMult : i >= 12 ? 1 + (warMult - 1) * 0.5 : 1;
    const rng = 1 + (Math.sin(i * 17 + baseline * 31) * 0.5 + 0.5 - 0.5) * noise;
    const drift = 1 + i * 0.003;
    return { date, price: parseFloat((baseline * drift * warEffect * rng).toFixed(4)) };
  });
}

function makeCountry(
  id: string, nameKo: string, region: string, lat: number, lng: number,
  // 실제 가격 (GlobalPetrolPrices.com 2026-03-16 기준)
  gasReal: number, dieselReal: number, elecReal: number, elecIReal: number,
  ngReal: number,  lpgReal: number,
  // 전쟁 영향 배수 (1.0 = 영향 없음, 1.5 = +50%)
  warGas: number, warDiesel: number, warElec: number, warNG: number
): Country {
  // baseline = 전쟁 전 추정값 (current / warMult)
  const gasBase    = parseFloat((gasReal   / warGas).toFixed(4));
  const dieselBase = parseFloat((dieselReal / warDiesel).toFixed(4));
  const elecBase   = parseFloat((elecReal  / warElec).toFixed(4));
  const ngBase     = parseFloat((ngReal    / warNG).toFixed(4));
  const lpgBase    = lpgReal > 0 ? parseFloat((lpgReal / warGas).toFixed(4)) : 0;

  return {
    id, nameKo, region, lat, lng,
    prices: {
      gasoline: {
        baseline: gasBase,
        current: gasReal,
        unit: 'USD/L',
        history: genHistory(gasBase, warGas),
      },
      diesel: {
        baseline: dieselBase,
        current: dieselReal,
        unit: 'USD/L',
        history: genHistory(dieselBase, warDiesel),
      },
      lng: {
        baseline: ngBase > 0 ? ngBase : parseFloat((gasBase * 12).toFixed(2)),
        current: ngReal > 0 ? ngReal : parseFloat((gasReal * 10).toFixed(2)),
        unit: 'USD/MMBtu',
        history: genHistory(ngBase > 0 ? ngBase : gasBase * 12, warNG),
      },
      elec_residential: {
        baseline: elecBase,
        current: elecReal,
        unit: 'USD/kWh',
        history: genHistory(elecBase, warElec),
      },
      elec_industrial: {
        baseline: parseFloat((elecBase * 0.82).toFixed(4)),
        current: parseFloat((elecReal * 0.82).toFixed(4)),
        unit: 'USD/kWh',
        history: genHistory(parseFloat((elecBase * 0.82).toFixed(4)), warElec),
      },
      lpg: {
        baseline: lpgBase,
        current: lpgReal > 0 ? lpgReal : 0,
        unit: 'USD/L',
        history: genHistory(lpgBase, warGas),
      },
    },
  };
}

